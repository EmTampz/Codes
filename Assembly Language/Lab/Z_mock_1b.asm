	.ORIG x3100
	LDI R1, FIRSTMULT
	LDI R2, SECONDMULT
	AND R3, R3, #0
	JSR MULT
	AND R1, R1, #0
	ADD R1, R1, R0
	AND R0, R0, #0
	LDI R2, THIRDMULT
	AND R3, R3, #0
	JSR MULT
	AND R1, R1, #0
	ADD R1, R1, R0
	AND R0, R0, #0
	LDI R2, FOURTHMULT
	AND R3, R3, #0
	JSR MULT
	STI R0, PRODUCT
	HALT


MULT 	
	NOT R2, R2
	ADD R2, R2, #1
LOOP	ADD R4, R3, R2
	BRz ENDLOOP
	ADD R0, R0, R1
	ADD R3, R3, #1
	BR LOOP

ENDLOOP RET


